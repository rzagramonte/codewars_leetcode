//declare a constant variable that will be assigned to an arrow function
//convert s to all lowercase
//split s by character and pass in each character as an element in a new array
//iterate over the array
//each letter will be reassigned to letter in uppercase concatenated by l repeated i many times where i is the index of the element
//join the new array passing in a dash as the separator
const accum=s=>s.toLowerCase().split('').map((l,i)=>l=`${l.toUpperCase()}${l.repeat(i)}`).join('-');